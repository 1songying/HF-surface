function point=f_length(x1,y1,c)
d1=sqrt((x1-116.685)^2+(y1-94.545)^2);
d2=sqrt((x1-196.19)^2+(y1-392.095)^2);
d3=sqrt((x1-313.355)^2+(y1-369.72)^2);
d4=sqrt((x1-373.765)^2+(y1-143.645)^2); 
row=1;
while((x1>=0)&&(x1<=500)&&(y1>=0)&&(y1<=500)&&(d1>=c)&&(d2>=c)&&(d3>=c)&&(d4>=c))
    dx1=9/25-(3*(x1/1250-1/5)*(y1/50+(x1/50-5)^2 -9))/2-(3*(y1/50-5)^2)/100-(3*x1)/5000;
    dy1=27/100-(3*(y1/1250-1/5)*(x1/50+(y1/50-5)^2-12))/2-(3*(x1/50-5)^2)/100-(3*y1)/5000;
    dx1_s=dx1/(dx1^2+dy1^2)^0.5;
    dy1_s=dy1/(dx1^2+dy1^2)^0.5;
    x2=x1+dx1_s*c;
    y2=y1+dy1_s*c;
    point(row,1)=x2;
    point(row,2)=y2;
    row=row+1;
    line([x1,x2],[y1,y2],'color','k','Linewidth',4);
    x1=x2;
    y1=y2;  
    d1=sqrt((x1-116.685)^2+(y1-94.545)^2);
    d2=sqrt((x1-196.19)^2+(y1-392.095)^2);
    d3=sqrt((x1-313.355)^2+(y1-369.72)^2);
    d4=sqrt((x1-373.765)^2+(y1-143.645)^2); 
end
end
